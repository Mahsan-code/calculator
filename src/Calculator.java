    private void HandleKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_HandleKeyPressed
      
         String keyText = KeyEvent.getKeyModifiersText(evt.getModifiers())+
                 
                  KeyEvent.getKeyText(evt.getKeyCode());
         
         switch (keyText){
            case "0":
                 btn0.doClick();
                 break;
            case "1":
                 btn1.doClick();
                 break;
            case "2":
                 btn2.doClick();
                 break;
            case "3":
                 btn3.doClick();
                 break;
            case "4":
                 btn4.doClick();
                 break;
            case "5":
                 btn5.doClick();
                 break;
            case "6":
                 btn6.doClick();
                 break;
            case "7":
                 btn7.doClick();
                 break;
            case "8":
                 btn8.doClick();
                 break;
            case "9":
                 btn9.doClick();
                 break;
            case "period":
                 btnDot.doClick();
                 break; 
            case "ShiftEquals":
                    btnPlus.doClick();
                    break;
            case "Minus":
                    btnMinus.doClick();
                    break;
            case "Shift8":
                    btnMultiply.doClick();
                    break; 
            case "Slash":
                    btnDivision.doClick();
                    break; 
            case "Escape":
                    btnClear.doClick();
                    break;
            case "Backspace":
            case "Delete":
                    btnDelete.doClick();
                    break;
            case "Enter":
                    btnEquals.doClick();
                    break;
            case "CtrlCtrl":
                    btnSign.doClick();
                    break;        
                    
                    
                   }
    }//GEN-LAST:event_HandleKeyPressed

