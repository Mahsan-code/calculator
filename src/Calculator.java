    double result = 0;
    
    private void BtnSignAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSignAction
        JButton btn = (JButton) evt.getSource();
        String str = txtDisplay.getText();
        switch(state){
            case "operator":
                str="";
                state = "operand2";
                break;
            case "result":
                str="";
                state = "operand1";
                break;
        
        }
      
        if (str.equals("0") && btn!=btnDot){
            str = "";
            }
         
        if (btn== btnDot){
         if (!str.contains(".")){
          str = str + ".";
         }
        }else{
        str = str + btn.getText();
        }
       
        
        txtDisplay.setText(str);
        
    }//GEN-LAST:event_BtnSignAction

    private void OperatorBtnAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OperatorBtnAction
      
        switch(state){
        
            case "operand1":
                operand1 = Double.parseDouble(txtDisplay.getText());
                System.out.println("operand1 = " + operand1);
                break;
            case "operand2":
                btnEqualsAction(null);
                operand1 = result;
                System.out.println("operand1 = " + operand1);
                break;
                
            case "result":
                operand1 = result;
                break;
                
        }
        
       state = "operator";
       
      JButton btn = (JButton) evt.getSource();
      
      if(btn==btnPlus) operator = "+";
      if(btn==btnMinus) operator = "-";
      if(btn==btnMultiply) operator = "*";
      if(btn==btnDivision) operator = "/";
        System.out.println("operator = " + operator);         
     
    }//GEN-LAST:event_OperatorBtnAction

    private void btnEqualsAction(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsAction
        
        switch(state){
            case "operand1":
                operand1 = Double.parseDouble(txtDisplay.getText());
                result = operand1;
                state = "result";
                return;
            case "operand2":
                operand2 = Double.parseDouble(txtDisplay.getText());
                System.out.println("operand2 = " + operand2);
                break;
                
            case "operator":
                operand2=operand1;
                break;
                
            case "result":
                operand1=result;
                break;
        }
        
        
        switch (operator){
        
            case "+":
                result = operand1+operand2;
                break;
            case "-":
                result = operand1-operand2;
                break;
            case "*":
                result = operand1*operand2;
                break;
            case "/":
                result = operand1/operand2;
                break;
             }   
            System.out.println("result=" + result);
            
            String strResult = String.valueOf(result);
            if (strResult.endsWith(".0")){
                strResult = strResult.substring(0, strResult.length()-2);
            }
            
            txtDisplay.setText(String.valueOf(strResult));
             
        
        state = "result";
                      
        
    }//GEN-LAST:event_btnEqualsAction

